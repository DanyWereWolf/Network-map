function openHelpModal() {
    var modal = document.getElementById('helpModal');
    var content = document.getElementById('helpModalContent');
    if (modal && content) {
        var v = (typeof APP_VERSION !== 'undefined') ? APP_VERSION : '—';
        var versionEl = document.getElementById('helpModalVersion');
        if (versionEl) versionEl.textContent = 'версия ' + v;
        content.innerHTML = getHelpContentHtml();
        modal.style.display = 'flex';
        document.body.style.overflow = 'hidden';
        requestAnimationFrame(function () {
            if (typeof window.initPanelPlexusCanvases === 'function') {
                window.initPanelPlexusCanvases(modal);
            }
        });
    }
}
function closeHelpModal() {
    var modal = document.getElementById('helpModal');
    if (modal) {
        modal.style.display = 'none';
        document.body.style.overflow = '';
    }
}

function helpSection(title, innerHtml) {
    return '<div class="help-section"><h3>' + title + '</h3>' + innerHtml + '</div>';
}

