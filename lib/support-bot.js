var DEFAULT_ANSWER =
    'Не нашёл точного ответа. Попробуйте спросить про лимиты, регистрацию или возможности карты. ' +
    'Либо нажмите «Написать владельцу» — разработчик ответит лично.';

function normalizeQuery(text) {
    return String(text || '')
        .toLowerCase()
        .replace(/[^\p{L}\p{N}\s]/gu, ' ')
        .replace(/\s+/g, ' ')
        .trim();
}

function matchFaq(query) {
    var q = normalizeQuery(query);
    if (!q) return { answer: DEFAULT_ANSWER, matched: false };
    var best = null;
    var bestScore = 0;
    FAQ_ENTRIES.forEach(function(entry) {
        var score = 0;
        entry.keys.forEach(function(k) {
            if (q.indexOf(k) !== -1) score += k.length;
        });
        if (score > bestScore) {
            bestScore = score;
            best = entry;
        }
    });
    if (best && bestScore > 0) {
        return { answer: best.answer, matched: true };
    }
    return { answer: DEFAULT_ANSWER, matched: false };
}

